   public void recibeIdDireccion (int idDireccion) throws SQLException{
       String sucursal = txtSucursal.getText();
       System.out.println("Recibe id Direccion " + idDireccion);
       String queryrecibedato = "INSERT INTO `sucursal`(`nombreSucursal`, `FK_nit`, `FK_idDireccion`) VALUES ('"+sucursal+"',999999991,"+idDireccion+");";
       System.out.println(queryrecibedato);
       
       try{
           connection = conexion.getConnection();
           st = connection.createStatement();
           st.executeUpdate(queryrecibedato);
           
           
       }catch(SQLDataException e){
           System.out.println(e);
       }
   }
   
   
